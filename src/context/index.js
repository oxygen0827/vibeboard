/**
 * Board-aware context API.
 *
 * All functions take boardId as first parameter — the system loads
 * the correct board definition and skills automatically.
 *
 * Usage:
 *   buildSystemPrompt('szpi_esp32s3', ['lvgl', 'wifi'])
 *   buildProjectFiles('szpi_esp32s3', 'my_app', ['lvgl'])
 *   patchSkill('szpi_esp32s3', 'lvgl', 'pitfall', '...')
 */

import { getBoard } from './boards/index'

// ── System Prompt Assembly ────────────────────────────────────

/** Build AI system prompt: base context + selected skills' prompts */
export function buildSystemPrompt(boardId, selectedSkillIds = []) {
  const board = getBoard(boardId)
  if (!board) throw new Error(`Unknown board: ${boardId}`)

  const skillPrompts = board.skills
    .filter(s => selectedSkillIds.includes(s.id))
    .map(s => s.systemPrompt)
    .filter(Boolean)
    .join('\n\n')

  return skillPrompts ? `${board.basePrompt}\n\n${skillPrompts}` : board.basePrompt
}

// ── Project File Generation ───────────────────────────────────

/**
 * Build complete project files for a given board + skills.
 * Returns a map of { 'filename': 'content' } plus metadata keys (__XXX).
 *
 * The output structure depends on the board's framework:
 *   - 'esp-idf' → CMakeLists.txt, sdkconfig.defaults, main/*.c
 *   - 'arduino' → sketch.ino, __libraries[], __boardFqbn
 *   - 'stm32cube' → Makefile, Src/main.c, Inc/, Drivers/
 */
export function buildProjectFiles(boardId, projectName, selectedSkillIds = []) {
  const board = getBoard(boardId)
  if (!board) throw new Error(`Unknown board: ${boardId}`)

  if (board.framework === 'arduino') {
    return buildArduinoProjectFiles(board, projectName, selectedSkillIds)
  }
  if (board.framework === 'stm32cube') {
    return buildStm32CubeProjectFiles(board, projectName, selectedSkillIds)
  }
  // Default: ESP-IDF
  return buildIdfProjectFiles(board, projectName, selectedSkillIds)
}

// ── ESP-IDF Framework ─────────────────────────────────────────

function buildIdfProjectFiles(board, projectName, selectedSkillIds) {
  const skills = board.skills.filter(s => selectedSkillIds.includes(s.id))
  const configs = skills.map(s => s.projectConfig).filter(Boolean)

  const needsCpp = configs.some(c => c.mainExt === 'cpp')
  const mainFile = needsCpp ? 'main.cpp' : 'main.c'
  const srcs = [mainFile, ...new Set(configs.flatMap(c => c.srcs || []))]

  const sdkBase = [
    '# Generated by esp32-vibe-coder',
    `CONFIG_IDF_TARGET="${board.idfTarget || 'esp32s3'}"`,
  ]
  // Flash size from board definition, default 16MB
  const flashSize = board.flashSize || '16MB'
  if (flashSize.endsWith('MB')) {
    sdkBase.push(`CONFIG_ESPTOOLPY_FLASHSIZE_${flashSize}=y`)
  }
  const sdkExtra = [...new Set(configs.flatMap(c => c.sdkconfig || []))]
  const sdkconfig = [...sdkBase, ...sdkExtra].join('\n')

  const components = [...new Set(configs.flatMap(c => c.idfComponents || []))]
  const idfComponentYml = components.length > 0
    ? `# IDF Component Manager Manifest File\ndependencies:\n` +
      components.map(c => `  ${c}`).join('\n') +
      `\n  idf:\n    version: ">=5.0"`
    : null

  const allPartitions = configs.map(c => c.partitions).filter(Boolean)
  function factorySize(lines) {
    const line = lines.find(l => l.includes('factory'))
    if (!line) return 0
    const m = line.match(/(\d+)M/)
    return m ? parseInt(m[1]) : 0
  }
  const partitionsCsv = allPartitions.length > 0
    ? allPartitions.reduce((a, b) => factorySize(a) >= factorySize(b) ? a : b).join('\n')
    : null

  const needsSpiffs = configs.some(c => c.spiffs)
  const compileOpts = [...new Set(configs.flatMap(c => c.compileOptions || []))]

  const srcList = srcs.map(s => `"${s}"`).join('\n                    ')
  let mainCmake = `idf_component_register(SRCS ${srcList}\n                    INCLUDE_DIRS ".")`
  if (needsSpiffs) {
    mainCmake += `\n\nspiffs_create_partition_image(storage ../spiffs FLASH_IN_PROJECT)`
  }
  if (compileOpts.length > 0) {
    mainCmake += `\ntarget_compile_options(\${COMPONENT_LIB} PRIVATE ${compileOpts.join(' ')})`
  }

  const rootCmake =
`cmake_minimum_required(VERSION 3.16)
include($ENV{IDF_PATH}/tools/cmake/project.cmake)
project(${projectName})`

  const files = {
    'CMakeLists.txt': rootCmake,
    'main/CMakeLists.txt': mainCmake,
    'sdkconfig.defaults': sdkconfig,
    '__mainFile': mainFile,
    '__framework': 'esp-idf',
    '__idfTarget': board.idfTarget || 'esp32s3',
  }
  if (idfComponentYml) files['main/idf_component.yml'] = idfComponentYml
  if (partitionsCsv)   files['partitions.csv'] = partitionsCsv

  return files
}

// ── Arduino Framework ─────────────────────────────────────────

function buildArduinoProjectFiles(board, projectName, selectedSkillIds) {
  const skills = board.skills.filter(s => selectedSkillIds.includes(s.id))
  const configs = skills.map(s => s.projectConfig).filter(Boolean)

  // Main entry: sketch.ino (Arduino standard)
  const mainFile = 'sketch.ino'

  // Collect Arduino libraries and build flags
  const libraries = [...new Set(configs.flatMap(c => c.arduinoLibraries || []))]
  const buildFlags = [...new Set(configs.flatMap(c => c.buildFlags || []))]
  const srcs = [mainFile, ...new Set(configs.flatMap(c => c.srcs || []))]

  const files = {
    [mainFile]: '',
    '__mainFile': mainFile,
    '__framework': 'arduino',
    '__boardFqbn': board.arduinoBoardId || '',
    '__libraries': JSON.stringify(libraries),
  }

  // Add additional source files as empty stubs
  srcs.filter(s => s !== mainFile).forEach(s => {
    if (!files[s]) files[s] = ''
  })

  if (buildFlags.length > 0) {
    files['__buildFlags'] = JSON.stringify(buildFlags)
  }

  return files
}

