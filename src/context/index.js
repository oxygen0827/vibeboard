  const incDirs = ['Inc', ...new Set(configs.flatMap(c => c.incDirs || []))]
    .map(d => `  ${d}`).join(' \\\n')
  const halSrcs = halModules.map(m => `  Drivers/STM32F1xx_HAL_Driver/Src/${m}.c`).join(' \\\n')
